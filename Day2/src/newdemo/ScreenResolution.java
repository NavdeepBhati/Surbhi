package newdemo;

import java.util.Optional;

public class ScreenResolution {

	private int width;
	private int height;
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public ScreenResolution(int width, int height) {
		super();
		this.width = width;
		this.height = height;
	}
	
	
	
}
  
